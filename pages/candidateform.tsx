export const getStaticProps = async () => {
    const res = await getCombo("genre", 0);
    const positionApplied: any[] = [];

    res.map((key:any) => {
        positionApplied.push({value: key.combo_key, label: key.combo_name})
    })

    return {
        props: {
            positionApplied
        }
    }
}

